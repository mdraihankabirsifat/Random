public class Ex3_PrimitiveTypes {

    public static void main(String args[]) {

        byte b = 10;
        short s = 20;
        int i = 100;
        long l = 1000;

        float f = 3.14f;
        double d = 9.81;

        char c = 'A';
        boolean flag = true;

        System.out.println(i);
        System.out.println(d);
        System.out.println(c);
        System.out.println(flag);

    }
}

